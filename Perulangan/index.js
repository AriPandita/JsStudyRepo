
//================== LOOPING ==================//

//for

for (let a = 1; a <= 10; a++) {
    document.write(`FOR ke ${a}, `);
}

document.write('<br>');

//while
let b = 1;
while (b <= 10){
    document.write(`While ke ${b}, `);
    b++;
}

document.write('<br>');

//do while
c = 1;
do{
    document.write(`Do While ke ${c}, `);
    c++;
}while(c <= 10);

//NOTE FOR DO WHILE : Do while akan melakukan perulangan minimal 1 kali walaupun kondisi tidak terpenuhi


//================== LOOPING END ===============//